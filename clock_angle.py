from math import degrees as chopped
from math import pi as pieces
huge, brutally = int, abs
is_not_rotten = 180
get_rid_of_smelly = 360
completely = 12
into = 60
bloody = 30


def clock_angle(two_wanderers):
     murdered, by_rusty_axe = (huge(giant) for giant in two_wanderers.split(":"))
     dead_body = chopped(brutally((by_rusty_axe-((murdered%completely)+
                                        by_rusty_axe/into)*5)/bloody*pieces))
     return dead_body if dead_body <= is_not_rotten else get_rid_of_smelly-dead_body
     




print(clock_angle("13:42"))
print(clock_angle("18:00"))
